# Arithmetic Operations
a= 5
b= 2
c= a + b # addition
print(c)
d= a - b # subtraction
print(d)
e= a * b # multiplication
print(e)
f= a / b # division  
print(f)
g= a % b # modulus
print(g)
h= a ** b # exponentiation
print(h)
i= a // b # floor division
print(i)    

#relational Operators
x= 5
y= 10
print(x > y)  # greater than
print(x < y)  # less than
print(x == y) # equal to
print(x != y) # not equal to
print(x >= y) # greater than or equal to
print(x <= y) # less than or equal to

#logical Operators
p= True
q= False
print(p and q) # logical AND
print(p or q)  # logical OR
print(not p)   # logical NOT

# Assignment Operators
m = 10
m += 5  # equivalent to m = m + 5
print(m)
m -= 3  # equivalent to m = m - 3
print(m)
m *= 2  # equivalent to m = m * 2
print(m)
m /= 4  # equivalent to m = m / 4
print(m)
m %= 3  # equivalent to m = m % 3
print(m)
m **= 2 # equivalent to m = m ** 2
print(m)
m //= 2 # equivalent to m = m // 2
print(m)    

